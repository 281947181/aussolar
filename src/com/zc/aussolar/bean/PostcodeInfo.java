package com.zc.aussolar.bean;

public class PostcodeInfo {
	private Integer vid;
	private String state;
	private String state_simple;
	private Integer from_code;
	private Integer to_code;
	public Integer getVid() {
		return vid;
	}
	public void setVid(Integer vid) {
		this.vid = vid;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getState_simple() {
		return state_simple;
	}
	public void setState_simple(String state_simple) {
		this.state_simple = state_simple;
	}
	public Integer getFrom_code() {
		return from_code;
	}
	public void setFrom_code(Integer from_code) {
		this.from_code = from_code;
	}
	public Integer getTo_code() {
		return to_code;
	}
	public void setTo_code(Integer to_code) {
		this.to_code = to_code;
	}
}
